// Addition

let age = 10 + 23;
console.log(age); // 33

// Subtraction

let points = 50 - 19;
console.log(points); // 31

// Multiplication
let score = 7 * 50;
console.log(score); // 350

// Division
let divide = 100 / 10;
console.log(divide); // 10

// Modulus (Remainder)
let mod = 6 % 3;
console.log(mod); // 0

mod = 7 % 3;
console.log(mod); // 1

// Exponentiation
let exp = 4 ** 2;
console.log(exp); // 16

exp = exp ** 2;
console.log(exp); // 256
