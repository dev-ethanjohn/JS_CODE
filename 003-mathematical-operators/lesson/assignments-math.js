// JS Mathematical Assignment Operators
let points = 10;
points++; // points = points + 1;
console.log(points); // 11

let score = 20;
score--;
score--;
console.log(score); // 18

score += 6;
console.log(score); // 24

score -= 50;
console.log(score); // -26

let age = 7;
age *= 2; // age = age * 2;
console.log(age); // 14

age /= 2;
console.log(age); // 7

// age = 7
age %= 7; // age = age % 7;
console.log(age); // 0

age **= 2;
console.log(age); // 0
